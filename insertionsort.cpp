 //--------------------  insertion_sort(long[],int)  ---------------------//
 /*************************************************************************/

 void insertion_sort(long array[],int array_size)
    {
       for(int i=1;i<array_size;i++)
	  {
	     long temp=array[i];

	     int j=i;

	     while(j>0 && temp<array[j-1])
		{
		   array[j]=array[j-1];
		   j--;
		}

	     array[j]=temp;
	  }
    }

